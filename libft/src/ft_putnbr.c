#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	char	m;

	m = 1;
	if (n == -2147483648)
	{
		n /= 10;
		ft_putnbr(n);
		write(1, "8", 1);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n *= (-1);
		}
		if (n / 10)
			ft_putnbr(n / 10);
		m = (n % 10) + 48;
		write(1, &m, 1);
	}
}
