#include "libft.h"

char	*ft_ctos(const char c)
{
	char	*s;

	if (c < 0)
		return (NULL);
	s = (char *)malloc(sizeof(char) * 2);
	s[0] = c;
	s[1] = '\0';
	return (s);
}
